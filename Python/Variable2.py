# Declaración de variables con diferentes tipos de datos
edad = 21
estatura = 1.70
nombre = "Ana"
estamosenclase = True

# Imprime el tipo de cada variable usando la función type()
print(type(edad))          # <class 'int'>
print(type(estatura))      # <class 'float'>
print(type(nombre))        # <class 'str'>
print(type(estamosenclase)) # <class 'bool'>

# Imprime una descripción utilizando las variables
print("Su edad es", edad, "su estatura es", estatura, "Su nombre es",
    nombre, "¿Hoy estamos en clase?", estamosenclase)